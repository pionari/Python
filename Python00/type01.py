# number : 숫자

# 정수형 (type : int)
a = 100
print(a)
print(type(a))
print(int(9.8))
print(int(7 / 6))
print(type(int('10')))

# <class 'int'> 파이썬은 모두 객체다

# 실수형 (type : float)
b = 100.1
print(b)
print(type(b))

print(float(4))
print(float(3 + 2))
print(type(float('1.2')))

# 2진수(0b), 8진수(0o), 16진수(0x) // binary, octa, hexa
c = 0b1111
print(c)
b = 0o77
print(b)
e = 0xff
print(e)
